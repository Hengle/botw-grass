class Grass:
    def __init__(self, height: float, r: int, g: int, b: int):
        """
        Creates a new instance of a Grass object

        :param height:  Height of grass
        :param r:       Red color component
        :param g:       Green color component
        :param b:       Blue color component
        """
        self.height = height
        self.r = r
        self.g = g
        self.b = b
